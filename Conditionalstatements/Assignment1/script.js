// 1. Purpose of if
// if is used to run code only when a condition is true

// 2. number > 10
let i = 12;
if (i > 10) {
  console.log("Greater");
} else {
  console.log("Smaller");
}

// 3. even or odd
let j = 7;
if (j % 2 === 0) {
  console.log("Even");
} else {
  console.log("Odd");
}

// 4. role of else
// else runs when if condition is false

// 5. voting eligibility
let k = 17;
if (k >= 18) {
  console.log("Eligible");
} else {
  console.log("Not Eligible");
}

// 6. traffic signal
let signal = "green";
if (signal === "green") {
  console.log("Go");
} else {
  console.log("Stop");
}

// 7. positive or not
let l = -5;
if (l > 0) {
  console.log("Positive");
} else {
  console.log("Not Positive");
}

// 8. divisible by 2
let i2 = 8;
if (i2 % 2 === 0) {
  console.log("Divisible by 2");
} else {
  console.log("Not Divisible");
}

// 9. if false + else present
// else block executes

// 10. which block executes
let j2 = 5;
if (j2 > 3) {
  console.log("if block");   // runs when true
} else {
  console.log("else block"); // runs when false
}

// 11. step-by-step trace
let k2 = 2;
if (k2 > 3) {
  console.log("Greater");
} else {
  console.log("Smaller");
}
// k2=2 → 2>3 false → else runs → "Smaller"

// 12. comparison operators
let l2 = 15;
if (l2 >= 10) {
  console.log("Condition True");
} else {
  console.log("Condition False");
}